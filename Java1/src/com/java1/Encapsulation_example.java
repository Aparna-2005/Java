package com.java1;

public class Encapsulation_example {
	private int rollno;
	private String name;
	public void setrollno(int roll) {
		rollno = roll;
	}
	
	public int getrollno() {
		return rollno;
	}
	
	public void setname(String name1) {
		name = name1;
	}
	
	public String getname() {
		return name;
	}
	
public static void main(String[] args) {
}
}
